<template>
    <GridCell
        :editing-allowed="false"
        :current-page="currentPage"
        :row="rowIndex"
        :column="columnIndex"
        :locked="true"
        :action-cell="false">
        <Component
            :is="headerComponent"
            v-bind="headerComponentProps"
            :namespace="namespace"
            @sort="getData(path)"
            @focus="onFocus" />
    </GridCell>
</template>

<script>
import { GRID_HEADER_TYPE } from '~/defaults/grid';

export default {
    name: 'GridHeaderCell',
    components: {
        GridCell: () => import('@Core/components/Grid/GridCell'),
    },
    props: {
        namespace: {
            type: String,
            required: true,
        },
        currentPage: {
            type: Number,
            required: true,
        },
        columnIndex: {
            type: Number,
            required: true,
        },
        column: {
            type: Object,
            required: true,
        },
        path: {
            type: String,
            required: true,
        },
        isColumnEditable: {
            type: Boolean,
            default: true,
        },
    },
    data() {
        return {
            rowIndex: 0,
        };
    },
    computed: {
        gridState() {
            return this.$store.state[this.namespace];
        },
        headerComponent() {
            const { type } = this.column.header;

            if (type === GRID_HEADER_TYPE.PLAIN) return () => import('@Core/components/Grid/PresentationCells/GridPresentationHeaderCell');

            return () => import('@Core/components/Grid/PresentationCells/GridPresentationInteractiveHeaderCell');
        },
        headerComponentProps() {
            const {
                title,
                hint,
                type,
                suffix,
            } = this.column.header;

            if (type === GRID_HEADER_TYPE.INTERACTIVE) {
                return {
                    columnIndex: this.columnIndex,
                    column: this.column,
                    isColumnEditable: this.isColumnEditable,
                };
            }

            return {
                title,
                hint,
                suffix,
            };
        },
    },
    methods: {
        getData(path) {
            this.$store.dispatch(`${this.namespace}/getData`, path);
        },
        onFocus(isFocused) {
            this.$emit('focus', isFocused);
        },
    },
};
</script>
